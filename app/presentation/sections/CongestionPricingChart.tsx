function parseCsv(csv: string): DataPoint[] {
  const lines = csv.trim().split("\n");
  const out: DataPoint[] = [];
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i];
    if (!line) continue;
    const [date, outside, inside] = line.split(",");
    const outsideNum = Number(outside);
    const insideNum = Number(inside);
    if (Number.isFinite(outsideNum) && Number.isFinite(insideNum)) {
      out.push({ label: date, outside_cbd: outsideNum, inside_cbd: insideNum });
    }
  }
  return out;
}

